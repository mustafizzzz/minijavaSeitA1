     public  void table_update() throws SQLException
    {
            int c;
      
             PreparedStatement insert;
            insert = conn.prepareStatement("select * from criminalinfo");
            ResultSet rs= insert.executeQuery();
            ResultSetMetaData res = (ResultSetMetaData) rs.getMetaData();
            c= res.getColumnCount();
            
            
            DefaultTableModel Df=(DefaultTableModel) CrminaljTable.getModel();
            Df.setRowCount(0);
            while(rs.next())
            {
                Vector v2=new Vector();
                for(int a=1; a<=c; a++){
                v2.add(rs.getString("crminal_id"));
                v2.add(rs.getString("fname"));
                v2.add(rs.getString("lname"));
                v2.add(rs.getString("age"));
                }
                Df.addRow(v2);
            }
    }
    

    
    
