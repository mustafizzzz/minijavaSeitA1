    private void criminaldeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_criminaldeleteActionPerformed
         try {
             String deletecriminal=JOptionPane.showInputDialog("Enter criminal Id to delete");
             
             PreparedStatement pst;
             while(deletecriminal.isEmpty()){
                 JOptionPane.showMessageDialog(null, "Check ID","Apocalyptic message", JOptionPane.WARNING_MESSAGE);
                 deletecriminal=JOptionPane.showInputDialog("Enter criminal Id to delete");
             }
             pst=conn.prepareStatement("Select * from criminalinfo Where crminal_id=?");
             pst.setString(1, deletecriminal);
             
             ResultSet res=pst.executeQuery();
             
             if(res.next())
             {
                 pst=conn.prepareStatement("delete from criminalinfo where crminal_id=?");
                 pst.setString(1, deletecriminal);
                 pst.execute();
                 JOptionPane.showMessageDialog(null, "Deleted Successfully");
                 table_update();
                 
             }else{
                 JOptionPane.showMessageDialog(this, "Police ID not Found","Error",JOptionPane.ERROR_MESSAGE);
             }
         } catch (SQLException ex) {
             Logger.getLogger(Criminalpage.class.getName()).log(Level.SEVERE, null, ex);
         }
       
    }//GEN-LAST:event_criminaldeleteActionPerformed

