    public  void table_update() throws SQLException
    {
            int c;
      
             PreparedStatement insert;
            insert = conn.prepareStatement("select * from rehabinfo");
            ResultSet rs= insert.executeQuery();
            ResultSetMetaData res = (ResultSetMetaData) rs.getMetaData();
            c= res.getColumnCount();
            
            
            DefaultTableModel Df=(DefaultTableModel) rehabjTable.getModel();
            Df.setRowCount(0);
            while(rs.next())
            {
                Vector v2=new Vector();
                for(int a=1; a<=c; a++){
                v2.add(rs.getString("criminal_id"));
                v2.add(rs.getString("duration"));
                v2.add(rs.getString("mentor"));
                }
                Df.addRow(v2);
            }
    }
    
    
    
    
    
    
    
    
    
