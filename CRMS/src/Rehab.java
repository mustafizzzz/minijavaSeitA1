    private void deletrehabActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletrehabActionPerformed
       String deleterehab=JOptionPane.showInputDialog("Enter criminal Id to delete");
       
       PreparedStatement pst;
       while(deleterehab.isEmpty()){
        JOptionPane.showMessageDialog(null, "Check ID","Apocalyptic message", JOptionPane.WARNING_MESSAGE);
        deleterehab=JOptionPane.showInputDialog("Enter criminal Id to delete");
       }
        try {
            pst = conn.prepareStatement("delete from rehabinfo where criminal_id=?");
            pst.setString(1, deleterehab);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null,"Deleted Successfully");
            table_update();
        } catch (SQLException ex) {
            Logger.getLogger(Rehab.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_deletrehabActionPerformed

